    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String AppointmentID = txtAppointmentID.getText();
        String CustomerName = cmbCustomerName.getSelectedItem().toString();
        String CategoryName = cmbCategoryName.getSelectedItem().toString();
        String TechnicianName = cmbTechnicianName.getSelectedItem().toString();
        String CarPlateNo = txtCarPlateNo.getText().toUpperCase();
        String AppointmentDetail = txtAppointmentDetails.getText();
        String AppointmentDate = txtAppointmentDate.getSelectedDate();
        String StartTime = (txtHours.getSelectedItem().toString() + cmbMinutes.getSelectedItem().toString());
        String EndTime = lblEndTime.getText();
        String status = "PENDING";

        Validation check = new Validation();
        Customer customer = new Customer();
//        Appointment Appointment = new Appointment(AppointmentID, CustomerName, CategoryName, TechnicianName, CarPlateNo, AppointmentDetails, AppointmentDate, StartTime, EndTime, "PENDING");
        try {
            if (check.checkAppointmentDate(AppointmentDate)) {
                if (!CarPlateNo.isEmpty() && !AppointmentDetail.isEmpty() && !TechnicianName.isEmpty() && !CategoryName.isEmpty() && !CustomerName.isEmpty()) {
//                    Appointment.Add();
                    ArrayList<String> ApppointmentDetails;
                    ApppointmentDetails = new ArrayList<>();
                    ApppointmentDetails.add(AppointmentID);
                    ApppointmentDetails.add(CustomerName);
                    ApppointmentDetails.add(CategoryName);
                    ApppointmentDetails.add(TechnicianName);
                    ApppointmentDetails.add(CarPlateNo);
                    ApppointmentDetails.add(AppointmentDetail);
                    ApppointmentDetails.add(AppointmentDate);
                    ApppointmentDetails.add(StartTime);
                    ApppointmentDetails.add(EndTime);
                    ApppointmentDetails.add(status);
                    Appointment appointment = new Appointment(ApppointmentDetails);
                    appointment.Add();
                    customer.UpdateAppointment(CustomerName, AppointmentID);
                    JOptionPane.showMessageDialog(rootPane, "One appointment is added", "Appointment Added", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    new FrmAppointment().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Please make sure that you have entered all the fields!");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Please make sure that you have chosen the day after.");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnCreateActionPerformed

