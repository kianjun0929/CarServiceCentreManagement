    private void RemoveTechnician() {
        da.setFileName("Appointment.txt");
        TotalRecords TR = new TotalRecords();
        String[] OccupiedTechnician = new String[TR.GetTotalAppointment()];
        String AppointmentDate = txtAppointmentDate.getSelectedDate().toString();
        String StartTime = txtHours.getSelectedItem().toString() + cmbMinutes.getSelectedItem().toString();
        int startTime = Integer.parseInt(StartTime);
        ArrayList<String> TechnicianWithAppointment = da.readAll();
        for (int i = 0; i < TechnicianWithAppointment.size(); i++) {
            String[] split = TechnicianWithAppointment.get(i).split("\\|");
            if (split[9].equals("PENDING")) {
                int EndTime = Integer.parseInt(split[8]);
                if (split[6].compareTo(AppointmentDate) == 0) {
                    if (EndTime > startTime) {
                        OccupiedTechnician[i] = split[3];
                    }
                }
            }

        }
        for (String rec : OccupiedTechnician) {
            cmbTechnicianName.removeItem(rec);
        }

    }

    public void CurrentDateandTime() {
        Thread clock = new Thread() {
            public void run() {
                try {
                    for (;;) {
                        Calendar cal = new GregorianCalendar();
                        int month = cal.get(Calendar.MONTH);
                        int year = cal.get(Calendar.YEAR);
                        int day = cal.get(Calendar.DAY_OF_MONTH);
                        txtDate.setText("Date: " + day + "/" + (month + 1) + "/" + year);
                        int hour = cal.get(Calendar.HOUR);
                        int minute = cal.get(Calendar.MINUTE);
                        int second = cal.get(Calendar.SECOND);
                        txtTime.setText("Time: " + hour + ":" + (minute) + ":" + second);
                        sleep(1000);
                    }
                } catch (Exception e) {
                    e.getCause();
                }
            }
        };
        clock.start();

    }

