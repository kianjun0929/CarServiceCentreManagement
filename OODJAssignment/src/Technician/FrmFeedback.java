    private void getDetails(String AppointmentID) {
        da.setFileName("Appointment.txt");
        ArrayList<String> data = da.readAll();
        for(String rec: data) {
            String[] split = rec.split("\\|");
            if(split[0].equals(AppointmentID)) {
                txtAppointmentID.setText(split[0]);
                txtCustomerName.setText(split[1]);
                txtCategoryName.setText(split[2]);
                txtTechnicianName.setText(split[3]);
            }
        }
    }

