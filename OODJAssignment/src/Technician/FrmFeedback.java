    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:

        String FeedbackID = txtFeedbackID.getText();
        String CategoryName = txtCategoryName.getText();
        String FeedbackDescription = txtFeedbackDescription.getText();
        String Improvement = txtImprovement.getText();
        String DateAdded = txtDateAdded.getText();
        String CustomerName = txtCustomerName.getText();
        String TechnicianName = txtTechnicianName.getText();
        String Rating = Integer.toString(Rate);
        
        ArrayList<String> FeedbackDetails;
        FeedbackDetails = new ArrayList<>();
        FeedbackDetails.add(FeedbackID);
        FeedbackDetails.add(AppointmentID);
        FeedbackDetails.add(CategoryName);
        FeedbackDetails.add(FeedbackDescription);
        FeedbackDetails.add(Rating);
        FeedbackDetails.add(Improvement);
        FeedbackDetails.add(DateAdded);
        FeedbackDetails.add(CustomerName);
        FeedbackDetails.add(TechnicianName);
        
        try {
            if(!FeedbackDescription.isEmpty() && !Improvement.isEmpty()) {
                Feedback feedback = new Feedback(FeedbackDetails);
//                Feedback feedback = new Feedback(FeedbackID, AppointmentID, CategoryName, FeedbackDescription, Rating, Improvement, DateAdded, CustomerName, TechnicianName);
                feedback.Add();
                new FrmPayment().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Please make sure that you give us Improvement and Feedback Description as feedback !");
            }
            
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

